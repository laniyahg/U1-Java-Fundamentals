public class Average2 {
    /*
    Given two numbers, num1 and num2, output the average.
    */

    public static void main(String[] args) {

        int num1  = 45;
        int num2  = 40;


        int averageTotal = num1 + num2;

        int totalAverage = averageTotal/2;

        System.out.println("Total average is: " + totalAverage);


    }

}
